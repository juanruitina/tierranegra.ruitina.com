<div class="seccion" id="start">
   <p>Durante dos décadas, la minería del carbón ha disminuido su actividad según lo previsto. En 1997, el carbón empleaba a uno de cada cuatro trabajadores en las Cuencas. Hoy, tan solo al 5%. La actividad aún persiste, pero la mayoría de los mineros se ha prejubilado. También muchos jóvenes se han beneficiado de las becas de estudios. Las principales infraestructuras están terminadas. Centenares de empresas han recibido fondos para sacar adelante sus proyectos en la Cuenca. Pero ¿han funcionado los fondos mineros? ¿Se han creado puestos de trabajo? ¿Está hoy la economía comarcal preparada para despedirse del carbón?</p>
   <p>La financiación de proyectos empresariales generadores de empleo, uno de los pilares de los planes de desarrollo alternativo de las Cuencas, no ha sido un gran éxito. De los cincuenta y dos proyectos participados por Hunosa, ha fracasado una cuarta parte. Estos proyectos recibieron 106 millones de euros en ayudas, no solo de fondos mineros, sino también del Principado de Asturias y del Instituto de Desarrollo Económico del Principado, el IDEPA. Hunosa ha conseguido recuperar las cantidades prestadas, pero las ayudas se concedieron a fondo perdido.</p>
