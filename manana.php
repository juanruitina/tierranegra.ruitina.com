<div class="seccion">
   <p>Algunas de estas empresas fueron tildadas de &ldquo;cazasubvenciones&rdquo;. La falta de controles adecuados antes y después de la adjudicación de las ayudas favoreció que compañías cuya viabilidad era, a la luz de los hechos, muy discutible se beneficiaran de decenas de miles de euros por cada puesto de trabajo. En muchos casos, no lograron mantener más allá del tiempo mínimo requerido de cinco años. Es el caso de Autotex, que así evitó devolver las ayudas. Al margen de las posibles intenciones ilícitas de algunas de esas empresas, es posible que algunos proyectos se impulsaran ante las optimistas perspectivas de crecimiento a las que puso fin la crisis de 2008. En cualquier caso, el final de la historia para demasiados trabajadores fue el despido, seguido de la lucha en los tribunales. Estas reducciones de plantilla sí fueron traumáticas.</p>
   <p>Por otro lado, los fondos para infraestructuras fueron algunas veces la excusa para reducir el gasto autonómico en los municipios mineros, lo cual negaba los principios de adicionalidad y complementariedad bajo los que se otorgaron las ayudas. "Lo que no encaja, por ejemplo", señala Alperi, "es cómo se pueden pagar los saneamientos de muchos núcleos urbanos del Nalón con fondos mineros, cuando en otros sitios se hacen con cargo de los presupuestos generales".</p>
   <p>No es sorprendente que, cuando apenas quedan tres años para el cierre, los resultados sean más bien decepcionantes.</p>
