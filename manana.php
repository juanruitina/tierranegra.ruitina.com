<div class="seccion">
   <p>
    Algunas de estas empresas fueron tildadas de &ldquo;cazasubvenciones&rdquo;. Por ejemplo, Autotex cerró el mismo día en que cumplía el compromiso de mantener los puestos de trabajo durante cinco años, evitando así devolver las ayudas. La falta de controles adecuados antes y después de la adjudicación de las ayudas favoreció que compañías cuya viabilidad era, a la luz de los hechos, muy discutible se beneficiaran de decenas de miles de euros por cada puesto de trabajo, que, en muchos casos, no lograron mantener más allá del tiempo mínimo requerido. Al margen de las posibles intenciones ilícitas de algunas de esas empresas, es posible que algunos proyectos se impulsaran ante las optimistas perspectivas de crecimiento a las que puso fin la crisis de 2008. En cualquier caso, el final de la historia para demasiados trabajadores fue el despido, seguido de la lucha en los tribunales. Estas reducciones de plantilla sí fueron traumáticas.</p>
   <p>
    ¿Y todo lo demás? Las inversiones en infraestructuras, formación, investigación y desarrollo tenían el mismo objetivo de mejorar las perspectivas de futuro de las Cuencas. Sin embargo, cuando apenas quedan tres años para el cierre, los resultados son más bien decepcionantes.</p>
   <p>
    Gráficos</p>
   <p>
    La ligera mejora del empleo en las Cuencas (un 4%) no se compensa ni de lejos con la pérdida de población en las últimas décadas (un 17%). De hecho, sus municipios siguen liderando en desempleo. La mitad del camino andado desde la aprobación del primer plan de desarrollo hasta 2007 se ha perdido desde el estallido de la crisis, al mismo tiempo que la población caía de forma aún más acusada.</p>
   <p>
    Además, es necesario mirar más allá: conforme la minería se replegaba en los últimos años crecía proporcionalmente el sector servicios, altamente dependiente de las pensiones. En junio de 2015, los jubilados de la minería cobraban un 75% más que el jubilado medio. De hecho, las pensiones del carbón suponen una tercera parte de la renta comarcal, y un 40% de esas pensiones es percibido por mayores de 75 años.</p>
