<div class="seccion centrada">
   <p>Algún día antes del 31 de diciembre de 2018 el último pozo echará el cierre, poniendo así fin a casi dos siglos de actividad minera. La minería dio empleo durante décadas, atrajo a inmigrantes de toda España y creó riqueza. Por muy pobre que fuera la calidad del carbón asturiano, los mineros fueron pieza fundamental de la industrialización española, y también cruciales en la lucha por los derechos laborales y sociales de todo el país que aún hoy se conservan.</p>
   <p>No hace falta ser adivino para ver cómo serán las Cuencas entonces. Con la gran mayoría de los mineros ya prejubilados —afortunadamente con mejor salud que sus antepasados—, sus rentas desaparecerán progresivamente con ellos. Las iniciativas por crear puestos de trabajo han sido insuficientes para cubrir los empleos de la minería, por lo que el éxodo hacia las ciudades de la comunidad seguirá mermando su demografía. La demanda generada por las pensiones sostendrá el nutrido sector servicios comarcal solo un par de décadas más. Si las tendencias no consiguieron invertirse ni con el empleo de las milmillonarias ayudas, es improbable que se logre en los tres años que quedan, cuando el plan del carbón vigente ya lo es de cierre.</p>
   <p>Las cifras y la economía son a veces frías, pero tras estas se encuentran muchas personas: trabajadores, pensionistas, jóvenes, políticos, contribuyentes. Todos pueden sentirse decepcionados. Algunos, incluso culpables. El intento de reconversión ha sido fallido a todas luces. Y solo las palabras pueden explicar su mayor suspenso: la gestión política. No es de extrañar que las políticas de desarrollo hayan sido un fracaso estrepitoso. El monopolio del poder político y sindical en Asturias ha sido la herramienta, pero también un veneno sin antídoto. Si todos los colectivos afectados hubiesen comprendido el elevado coste de oportunidad de estas malas prácticas y los agentes implicados en la toma de decisiones hubiesen sido más diversos, seguramente habrían demandado un mayor control del uso de los fondos y habrían corregido las deficiencias de los planes del carbón durante esos veintisiete años en que pudieron hacerlo. Incluso podrían haberse discutido e intentado otras formas de mejorar sus perspectivas de futuro.</p>
   <p>Quizá los proyectos de I+D+i deberían haber recibido más que una parte marginal de los fondos, frente a los cientos de millones en infraestructuras cuya utilidad nadie discute, pero cuyo carácter prioritario es cuestionable. Quizá aceptar la migración interna habría animado a llevar a cabo más iniciativas de las que se beneficiara toda la comunidad: el eje Oviedo-Gijón-Avilés, cuyo tejido económico y laboral es más sólido, podría haber atraído talento si se hubiese planteado seriamente el objetivo de convertirlo en un polo de innovación y crecimiento.</p>
   <p>Pero quién sabe: de haber estado el poder repartido entre más manos, quizá ni siquiera se hubieran conseguido las ayudas. Esta paradoja devolverá a las Cuencas Mineras a su punto de partida. En la nochevieja de 2018, el carbón se despedirá puntual y con tranquilidad de aquellos que lo trabajaron y lo sufrieron, pero el futuro de las Cuencas no será mejor tras esa última campanada. Huérfanas, se dormirán poco a poco conforme se vaya disipando el espejismo que provocaron sus últimos años de artificial prosperidad.</p>
