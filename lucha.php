 <p>
 A principios del siglo XX empieza a gestarse el movimiento sindical minero, crucial para entender el desarrollo de las Cuencas hasta hoy día. En 1910, Manuel Llaneza funda el Sindicato de Obreros Mineros de Asturias, el SOMA, hoy parte de UGT. Poco después tuvieron lugar las primeras grandes huelgas: primero para ser reconocidos, después para conseguir objetivos laborales, tales como el salario mínimo o la semana de vacaciones. Durante la Segunda República, los sindicatos fueron protagonistas de la revolución de 1934. En la Guerra Civil también se posicionaron en el bando republicano, pero el precio fue alto: desde la caída del norte en el bando nacional y durante la posguerra, los trabajadores mineros fueron militarizados, hasta el punto de que &ldquo;el abandono del trabajo sería interpretado como deserción, la falta de disciplina como insubordinación y la huelga como rebelión militar&rdquo;.</p>
<p>
 En la década de los 50, el cada vez mayor descontento favoreció la removilización obrera, organizada en la clandestinidad pero adaptándose a las insuficientes estructuras de participación construidas por el régimen franquista. Tras modestas huelgas y ya de lleno en el desarrollismo, en 1962 tuvo lugar la &ldquo;huelgona&rdquo;. El régimen fue incapaz de reprimir una protesta que empezó con apenas siete picadores en el pozo Nicolasa y que en dos meses se había propagado ya por toda España, con hasta cien mil trabajadores movilizados y numerosas muestras de solidaridad incluso de intelectuales de la época.</p>
<h3>El declive de la minería</h3>
<p>Al margen de las reivindicaciones obreras, las empresas mineras atravesaban un mal momento en lo financiero. Cuando la autarquía deja paso a una mayor liberalización del comercio exterior, las empresas perdieron parte del mercado ante el carbón extranjero, como siempre más barato y de mejor calidad. Un lustro después de la &ldquo;huelgona&rdquo;, en 1967, quince compañías privadas de la minería asturiana se fusionan para formar Hulleras del Norte, S.A., Hunosa. El estatal Instituto Nacional de Industria tiene una participación inicial del 77%, aunque en 1970 se convierte en accionista único. Desde entonces, las constantes pérdidas de Hunosa son asumidas en los Presupuestos Generales del Estado.</p>
<p>
 Con el sector ya casi íntegramente nacionalizado, es el Estado quien dirige casi en solitario la minería del carbón en Asturias. No es poca responsabilidad: es en ese momento cuando se materializó la necesidad de reconvertir la economía regional. Ya en democracia, los sindicatos —legalizados—, y los sucesivos gobiernos empezaron a negociar la gestión del declive minero. Tras la adhesión a las Comunidades Europeas en 1986, España tuvo que asumir el marco legislativo comunitario a la hora de configurar los planes del carbón.</p>
<h3>Los planes del carbón</h3>
<p>Las ayudas a la producción de carbón permiten mantener aún hoy la actividad minera, que emplea todavía a unas 2.500 personas de forma directa en Asturias. Por otro lado, las prejubilaciones permiten a los mineros retirarse a edades tan tempranas como los 42 años —en función de la crudeza de su puesto de trabajo— con pensiones elevadas. Así se ha podido reducir la plantilla de Hunosa de forma no traumática, y también mejorar la salud de los trabajadores.</p>
<p>
 Pero a partir del Plan del Carbón 1998-2005 las ayudas tuvieron un objetivo más ambicioso: reconvertir la economía de las Cuencas. Con ese fin surgen los célebres Fondos Mineros: por una parte ayudas estatales autorizadas por la Unión Europea y por otra los fondos comunitarios de desarrollo regional (FEDER). Estos fondos fueron destinados a infraestructuras, proyectos generadores de puestos de trabajo, becas formativas y proyectos de investigación y desarrollo. Eso sí: a cambio, de autorizar estas ayudas, la Comisión Europea exige que las explotaciones mineras no rentables cierren para el año 2018. De lo contrario deberán devolverse las ayudas, 22.000 millones de euros según el gobierno central.</p>
