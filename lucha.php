<p>
 Pero el trabajo era duro. Las condiciones laborales eran cercanas a la esclavitud: a las jornadas de 12 horas, los hasta 310 días de trabajo al año y el trabajo a destajo, se añadía la dureza propia de la labor minera: la exposición a la humedad, la inhalación de polvos —causante de la temida silicosis— y la alta siniestralidad. Todo por un sueldo mínimo, que con frecuencia se completaba con las labores agrícolas. Desde muy pronto, los trabajadores empezaron a manifestar su disconformidad con estas condiciones. Primero lo hacían de forma individual, mediante sabotajes o el absentismo. Pero con la progresiva llegada de inmigrantes, sin arraigo ni tierras que cultivar, y con la difusión de las ideas socialistas, los mineros comenzaron a movilizarse.</p>
