<p>Cuando Jovellanos advertía del potencial del carbón asturiano, estaba en lo cierto. La historia reciente de Asturias, y en especial la de las Cuencas Mineras, está ligada al florecimiento de la industria regional: la siderurgia y la minería del carbón supusieron toda una revolución económica, demográfica y cultural. Aunque se tenía constancia de la existencia de yacimientos en la región desde hacía siglos, las explotaciones solo empezaron a adquirir relevancia en el siglo XIX. Primero hizo falta mejorar las infraestructuras: desde que en 1842 Langreo se une con el puerto de Gijón por carretera, numerosas explotaciones empiezan a florecer en Mieres, Langreo y otros municipios cercanos. En torno a ellos, la siderurgia empieza a nutrirse del carbón extraido. Es el caso de Duro Felguera, una de las empresas más emblemáticas que aún mantiene parte de su actividad en Asturias.</p>

