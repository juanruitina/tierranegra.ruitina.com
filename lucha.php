        <h3>Guaje</h3>
        <p>Hoy día se discute si la  conocida palabra &ldquo;guaḥe&rdquo; (con hache aspirada),  &ldquo;niño&rdquo; en asturiano, viene del inglés  &ldquo;washer&rdquo; (&ldquo;lavador&rdquo;) o del alemán-neerlandés &ldquo;wagen&rdquo; (&ldquo;vagón&rdquo;). El lavado de carbón y el tirado de vagones de carbón eran precisamente las tareas de las que se encargaban habitualmente los niños.</p>
