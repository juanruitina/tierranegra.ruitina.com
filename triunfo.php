  <div id="start" class="seccion">
   <p>22.000 millones de euros en veinticinco años de ayudas: cuando un colectivo de 45.000 personas consigue esos fondos es difícil utilizar otra palabra que &ldquo;éxito&rdquo;. No solo supusieron un beneficio personal para los trabajadores, sino también para sus vecinos y las comarcas. Pero no se puede comprender lo ocurrido sin conocer el sustrato político a partir del cual germinó tal hazaña. La historia explica el gran poder que los sindicatos han conseguido en el sector y el sólido apoyo social. A posteriori, en democracia, los ha situado en una posición privilegiada para negociar los planes del carbón.</p>

   <p>La comunidad ha sido gobernada por el PSOE durante veintiocho de los treinta y tres años que han trascurrido desde la aprobación del Estatuto de Autonomía. Frente a la relativa alternancia política a nivel nacional, Asturias ha sido —y sigue siendo— uno de los bastiones del socialismo español. Como ocurre en otras regiones <a href="http://thelandofmaps.tumblr.com/post/118561621665/labor-results-for-the-2015-uk-parliamentary">tradicionalmente industriales y mineras</a>, el fenómeno se acentúa aún más en las Cuencas. Salvo por casos anecdóticos, PSOE e Izquierda Unida se han repartido las alcaldías de toda la comarca durante los últimos treinta y siete años.</p>

