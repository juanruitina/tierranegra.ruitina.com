   <p>En los últimos años, varias instituciones han cuestionado la gestión de los organismos encargados de los fondos. En 2010, la Sindicatura de Cuentas del Principado de Asturias afirmaba que la gestión de los fondos mineros era poco eficiente &ldquo;tanto desde el punto de vista operativo, como en su reflejo financiero&rdquo;, y destacaba los bajos niveles de ejecución de los proyectos presupuestados entre 2006 y 2009: entre el 50 y el 70% del total.</p>

