   <p>El dominio de la izquierda es la causa de los logros sociales, y lo es no tanto por estar en línea con los idearios de izquierdas —que también—, sino por el enorme poder político y electoral que los representantes obreros, y más concretamente mineros, han tenido en la política regional e incluso nacional.</p><p>
   Al igual que ocurriera a la hora de mantener la siderurgia en Asturias en los 80, el gran poder autonómico de UGT permitió repetidas concesiones en todo lo relativo a los fondos mineros. Para ilustrar este poder es suficiente con citar un nombre: <strong>José Ángel Fernández Villa</strong>. Secretario general del SOMA durante treinta y cuatro años, este langreano, que trabajó en Hunosa y Ensidesa, fue además miembro de la Ejecutiva Federal del PSOE, senador por Asturias y diputado en la Junta General del Principado. Se solía decir que era él quien decidía quién poner al frente de la Federación Socialista Asturiana —casi siempre sinónimo de designar la presidencia autonómica—. Todo parecía posible cuando su indiscutible liderazgo en cada pugna se sumaba al inigualable poder de movilización de la minería asturiana.</p>
   <h3>Sin control</h3>
   <p>El mismo gran poder que permitió obtener las ayudas del Estado tenía su lado oscuro. La opacidad y la falta de control de los fondos son transversales a cada ámbito de los planes del carbón. Aunque en los últimos años las administraciones públicas españolas han hecho un cierto esfuerzo por mejorar la transparencia y el libre acceso a la información, no se ha apreciado un cambio notable en las instituciones encargadas de los fondos mineros. El Instituto para la Reestructuración de la Minería (IRCM) aprobó en 2013 un plan de medidas para abrir un catálogo de datos al público ese mismo año: dos años después, aún no se puede encontrar una sola estadística en su sede electrónica.</p>

