   <p>Efectivamente, el gran número de agentes implicados —Gobierno, comunidad autónoma, ayuntamientos, sindicatos, empresas, organismos— sigue impidiendo un seguimiento eficaz del uso de los fondos. Un ejemplo que se puede juzgar de forma especialmente detallada es el de <strong>Fundesfor</strong>, la fundación encargada de la gestión de los fondos destinados a formación. En 2012, estando ya anunciado el cierre del organismo para finales de año, el Tribunal de Cuentas elevó al Congreso un informe de fiscalización que recogía las numerosas irregularidades en su gestión. El tribunal señalaba entonces que la fundación no monitorizó el cumplimiento de las obligaciones por parte de los becarios ni pidió justificantes de los gastos. Concedió ayudas a empresas que no cumplían los requisitos establecidos y tampoco les exigió garantías financieras. Ni siquiera reclamó el reintegro de las ayudas a las nueve empresas que subcontrataron la ejecución de los proyectos, lo cual estaba prohibido. El informe denuncia incluso cómo no se seguían las recomendaciones que el mismo tribunal había realizado en 2002: por ejemplo, la fundación seguía sin cruzar información con la totalidad de administraciones para evitar el fraude en las solicitudes.</p>

   <p>Ambas instituciones coinciden en señalar la falta de control en la gestión de las ayudas. Los objetivos a veces no eran claros; el control de su cumplimiento, muy deficiente, y pocas veces se evaluaron los resultados. Los puntos débiles de las ayudas podrían haberse subsanado en los veintitrés años que han cubierto los sucesivos planes del carbón. Sin embargo, las agentes implicados —políticos y ciudadanos— no demandaron de forma contundente rendición de cuentas, quizá cegados por la lluvia de millones.</p>

   <h3>La corrupción</h3>
   <p>Un viejo conocido de la justicia es <strong>Victorino Alonso</strong>. Coronado &ldquo;rey del carbón&rdquo; por los medios, este empresario leonés posee gran parte del sector privado de la minería del carbón y es el presidente de Carbunión, que aúna a la patronal. En 2010, acusado por un delito de fraude fiscal cometido en 1996, logró conmutar la cárcel por una elevada multa. En 2013, Hunosa se querelló contra dos de sus empresas por la desaparición de medio millón de toneladas de carbón del Almacén Estratégico Temporal (AETC) que custodiaban, por valor de 46 millones de euros. Ya en 2015, debe enfrentarse a una fianza de 66 millones de euros por destruir un yacimiento arqueológico en Huesca. Mientras, aplicaba un ERE temporal a toda su plantilla, de más de 800 empleados.</p>
   <p>Cuando el río suena, agua lleva. La mala gestión política de la reconversión va más allá de la negligencia. En octubre de 2014, la Fiscalía Anticorrupción anunció que estaba investigando al mismísimo Villa <a href="http://www.europapress.es/asturias/noticia-fiscalia-anticorrupcion-investiga-fernandez-villa-ocultar-hacienda-publica-14-millones-euros-20141008101211.html">por ocultar presuntamente 1,4 millones</a> de euros a Hacienda. El histórico sindicalista se había acogido a la amnistía fiscal de Rajoy, pero la fiscalía sospechaba que la procedencia de ese dinero era ilícita.</p>
   <p>El origen de esos fondos aún no está claro. Villa defendió que los 1,4 millones de euros provenían de la herencia de su madre, la cual regentaba un chigre en Tuilla (Langreo). Esta versión, poco creíble, fue desmentida por una de sus hermanas. Sin embargo, se apunta ya a tres fuentes: la participación del sindicalista en el comité intercentros de Hunosa, que le reportó 243.000 euros en gastos no debidamente justificados; las ayudas a cursos de formación a través de Fundesfor, cuyas cuentas, como denunció el Tribunal de Cuentas, no estaban claras, y el cobro fraudulento de comisiones por contratos para la construcción de una residencia para mayores propiedad del Montepío de la Minería, en el concejo de Aller. Según informaba <em>El País</em>, Villa, presidente del Montepío, consiguió en la fiesta minera de Rodiezmo (León) que José Luis Rodríguez Zapatero concediera subvenciones al organismo. Consiguió dar la dirección del Montepío a José Antonio Postigo, su amigo personal, y ambos colocaron a dos hijos con sendos contratos de 50.000 euros anuales y blindados con una anualidad en caso de despido. Al igual que Villa, Postigo y su hija regularizaron cientos de miles de euros con la amnistía fiscal del Partido Popular.</p>
   <p>También causó gran revuelo el testimonio de José Vía, representante del minoritario sindicato USO. En <a href="http://www.elcomercio.es/politica/201410/26/fiscalia-investiga-fondo-caso-20141026011707-v.html">una entrevista al diario gijonés <em>El Comercio</em></a>, Vía afirma haber presenciado conversaciones delicadas en las cuales se reconocía de forma implícita malversación de fondos y tráfico de influencias por parte de los representantes de, al menos, UGT.</p>

